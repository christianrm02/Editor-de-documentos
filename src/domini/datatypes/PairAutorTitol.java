package datatypes;

public class PairAutorTitol {
    private String autor;
    private String titol;

    public PairAutorTitol(String autor, String titol) {
        this.autor = autor;
        this.titol = titol;
    }

    public getAutor() {
        return autor;
    }

    public getTitol() {
        return titol;
    }
}